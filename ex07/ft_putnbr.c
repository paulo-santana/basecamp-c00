#include <unistd.h>

void	ft_putnbr(int nb)
{
	int digit;
	int is_min;

	is_min = 0;
	if (nb < 0)
	{
		write(1, "-", 1);
		if (nb == -2147483648)
		{
			is_min = 1;
			nb = -2147483647;
		}
		nb *= -1;
	}
	digit = nb % 10 + '0' + is_min;
	nb = nb / 10;
	if (nb > 0)
		ft_putnbr(nb);
	write(1, &digit, 1);
}
